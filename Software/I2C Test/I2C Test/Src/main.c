#include "main.h"
#include "stm32f3xx_hal.h"

I2C_HandleTypeDef hi2c1;
UART_HandleTypeDef huart2;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_USART2_UART_Init(void);
static void MX_I2C1_Init(void);

int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
	
	HAL_GPIO_WritePin(MPU6050_AD0_GPIO_Port, MPU6050_AD0_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(LED_MODE_GPIO_Port, LED_MODE_Pin, GPIO_PIN_SET);
	
	uint8_t command[] = {0x75};
	uint8_t response = 0;

  while (1)
  {
		HAL_I2C_Master_Transmit(&hi2c1, (0x69 << 1), command, 1, 100);
		HAL_I2C_Master_Receive(&hi2c1, ((0x69 << 1) | 0x01), &response, 1, 100);
		HAL_Delay(10);
  }
}

